  for (int i = 0; i < SPECTRUM_SIZE; i++)
    {
      ret.x += (cie_cmf_x[i] * s[i]);
      ret.y += (cie_cmf_y[i] * s[i]);
      ret.z += (cie_cmf_z[i] * s[i]);
      sum += cie_cmf_y[i];
    }
  luminosity_t scale = 1 / sum;
  ret.x *= scale;
